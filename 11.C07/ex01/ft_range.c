int	*ft_range(int	min, int	max)
{
	int	i;
	int	j;
	int* range = (int*) malloc ((max - min)*4);

	i = min;
	j = 0;
	if (min >= max)
	{
		return (0);
	}
	while (i >= min && i < max)
	{
		range[j] = i;
		i++;
		j++;
	}
	return (range);
}	
