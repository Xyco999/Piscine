#include <unistd.h>

char	*ft_strdup(char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		i++;
	}
	char* cpy = (char*)malloc (i * 1);
	i = 0;
	while (src[i] != '\0')
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';	
	return (cpy);
}
