#include <unistd.h>

void	ft_is_negative(int n)
{
	int	entered;

	entered = n;
	if (n < 0)
		write (1, "N", 1);
	else
		write (1, "P", 1);
}

void main(void)
{
	ft_is_negative(21);
}
